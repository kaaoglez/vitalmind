/**
 * Helper function to get a nested value from an object using dot notation.
 * Example: getNestedValue(obj, 'landing.hero.title')
 *
 * Supports the `_` convention: if a path resolves to an object (not a string),
 * and that object has a `_` key with a string value, that string is returned.
 * This allows a key like `achievements.first_log` to return a string while
 * `achievements.first_log.desc` also resolves to a nested string.
 */
export function getNestedValue(obj: Record<string, TranslationValue>, path: string): string | undefined {
  const keys = path.split('.');
  let current: TranslationValue | Record<string, TranslationValue> = obj;
  for (const key of keys) {
    if (current === null || current === undefined || typeof current !== 'object') {
      return undefined;
    }
    current = current[key];
  }
  if (typeof current === 'string') return current;
  // If resolved to an object, check for the `_` convention key
  if (current !== null && typeof current === 'object' && typeof (current as Record<string, TranslationValue>)['_'] === 'string') {
    return (current as Record<string, TranslationValue>)['_'];
  }
  return undefined;
}
